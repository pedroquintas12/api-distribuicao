public class RetornoReu {

    private int codPolo;
    private String nome;
    private String descricaoTipoPolo;
    private String cnpj;
    private String cpf;

    public int getCodPolo() {
        return codPolo;
    }

    public void setCodPolo(int codPolo) {
        this.codPolo = codPolo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescricaoTipoPolo() {
        return descricaoTipoPolo;
    }

    public void setDescricaoTipoPolo(String descricaoTipoPolo) {
        this.descricaoTipoPolo = descricaoTipoPolo;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
}
